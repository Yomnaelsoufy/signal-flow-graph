package control;

public class edge {
  private int from,to,weight;
    public edge(int from,int to,int  weight){
        this.weight=weight;
        this.to=to;
        this.from=from;
    }
    public int getWeight() {
        return weight;
    }
    public int getTo() {
        return to;
    }
    public
    int getFrom(){
        return from;
    }
}
